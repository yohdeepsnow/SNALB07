#tolong lengkapi fungsi python berikut untuk membuat kalkulator sederhana
#buat dengan format yang sama

def pembagian(a,b):
	total = a/b
	return(total)
	
def penambahan(a,b):
	total = a+b
	return(total)

def pengurangan(a,b):
	total = a-b
	return(total)

def main():
	print(pembagian(20,5))
	print(penambahan(10,5))
	print(pengurangan(1,2))

main()
