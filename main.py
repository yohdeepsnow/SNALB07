#tolong lengkapi fungsi python berikut untuk membuat kalkulator sederhana
#buat dengan format yang sama

def perkalian(a,b):
	total = a*b
	return(total)

def pembagian(a,b):
	total = a/b
	return(total)

def pengurangan(a,b):
	total = a-b
	return(total)

def penambahan(a,b):
	total = a+b
	return(total)

def main():
	print(perkalian(12,3))
	print(pembagian(15,3))
	print(pengurangan(20,15))
	print(penambahan(10,5))

main()
